package tutorialJava.capitulo7_Recursos.ejemploInsultoRecibidoListener;

public class SobreFraseEvent {
	private String fraseCompleta;
	private String insultoRecibido;
	
	public String getFraseCompleta() {
		return fraseCompleta;
	}
	public void setFraseCompleta(String fraseCompleta) {
		this.fraseCompleta = fraseCompleta;
	}
	public String getInsultoRecibido() {
		return insultoRecibido;
	}
	public void setInsultoRecibido(String insultoRecibido) {
		this.insultoRecibido = insultoRecibido;
	}
	
	
}
